function variant(seed: TemplateSeed, backgroundMode: BackgroundMode): TemplateDescriptor {
  return {
    ...seed,
    id: `${seed.id}-${backgroundMode}`,
    name: backgroundMode === 'dark' ? `${seed.name} Dark` : seed.name,
    description:
      backgroundMode === 'dark'
        ? `${seed.description} Adapted for darker surrounding UI and presentation backgrounds.`
        : seed.description,
    backgroundMode,
    tags: [...seed.tags, backgroundMode],
  };
}

export const templateCatalog: TemplateDescriptor[] = seeds.flatMap((seed) => [variant(seed, 'light'), variant(seed, 'dark')]);
